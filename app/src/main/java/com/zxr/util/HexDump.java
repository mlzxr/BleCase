    /**
     * byte数组转换为二进制字符串
     **/
    public static String byteArrToBinStr(byte[] b) {
        StringBuffer result = new StringBuffer();
        for (int i = 0; i < b.length; i++) {
            result.append(Long.toString(b[i] & 0xff, 2));
        }
        String str = result.toString().substring(0, result.length());
        return str;
    }


